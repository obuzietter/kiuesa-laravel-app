            <h2>Our History</h2>
            <p>
                KIUESA was founded in 2019 by a group of passionate students from various Kenyan universities who shared a common goal: to protect the environment and promote sustainability in their communities. What started as a small student-led initiative has now grown into a national organization with a network of over 1000 members across the country. Over the years, we have worked on numerous projects and campaigns to raise awareness about environmental issues, promote conservation efforts, and empower young people to take action for a greener future.
            </p>
            <p>
                Our journey has been filled with challenges and triumphs, but through it all, we have remained committed to our mission and vision. We are proud of the impact we have made so far and excited about the opportunities that lie ahead. As we continue to grow and evolve, we are more determined than ever to make a difference and create a better world for all.
